import {
  qs,
  qsa,
  on,
  toggleClass,
  isMobile,
  getScrollY,
  scrollToY,
} from "../../core/dom.js";
import { createDisposables } from "../../core/disposables.js";

export function initNavbar({
  // функция инициализации навбара, принимает объект с селекторами и классами для настройки поведения навбара
  navSelector, // селектор для навбара
  navLinksSelector, // селектор для контейнера с навигационными ссылками
  navIconSelector, // селектор для иконки меню (гамбургер)
  navItemSelector, // селектор для отдельных пунктов меню
  scrolledClass, // класс, который добавляется к навбару при прокрутке
  openClass, // класс, который добавляется к контейнеру с навигационными ссылками при открытии мобильного меню
  scrollOffset, // смещение для якорных ссылок при прокрутке
  mobileBreakpoint, // ширина экрана, при которой считается мобильным устройством
  scrolledThreshold, // порог прокрутки для добавления класса scrolledClass
} = {}) {
  // функция возвращает функцию для очистки обработчиков событий и классов при уничтожении компонента
  const nav = qs(navSelector); // находим элемент навбара по селектору
  const navLinks = qs(navLinksSelector); // находим элемент контейнера с навигационными ссылками по селектору
  const navIcon = qs(navIconSelector); // находим элемент иконки меню по селектору
  const navItems = qsa(navItemSelector); // находим все элементы пунктов меню по селектору

  if (!nav || !navLinks || !navIcon) return () => {}; // если не удалось найти необходимые элементы, возвращаем пустую функцию для очистки

  const d = createDisposables(); // создаем объект для управления обработчиками событий и классами

  const closeMenu = () => navLinks.classList.remove(openClass); // функция для закрытия мобильного меню, удаляет класс openClass у контейнера с навигационными ссылками
  const toggleMenu = () => navLinks.classList.toggle(openClass); // функция для переключения состояния мобильного меню, добавляет или удаляет класс openClass у контейнера с навигационными ссылками

  // mobile menu open/close
  d.add(
    // добавляем обработчик события клика на иконку меню для открытия или закрытия мобильного меню
    on(navIcon, "click", (e) => {
      // при клике на иконку меню
      e.stopPropagation(); // останавливаем распространение события, чтобы клик не обрабатывался обработчиком для кликов вне меню
      toggleMenu(); // переключаем состояние мобильного меню, открывая его, если оно было закрыто, или закрывая его, если оно было открыто
    }),
  );

  // close on outside click
  const navPanel = navLinks.querySelector("ul"); // находим элемент <ul> внутри контейнера с навигационными ссылками, который содержит пункты меню, для проверки кликов внутри этого элемента

  d.add(
    // добавляем обработчик события клика на документ для закрытия мобильного меню при клике вне его
    on(document, "click", (e) => {
      // при клике на документ
      const target = e.target; // получаем элемент, на который был произведен клик

      const insidePanel = navPanel?.contains(target); // проверяем, находится ли элемент, на который был клик, внутри панели с навигационными ссылками (например, внутри <ul>), используя метод contains для проверки вложенности элементов
      const insideIcon = navIcon.contains(target); // проверяем, находится ли элемент, на который был клик, внутри иконки меню, используя метод contains для проверки вложенности элементов

      if (!insidePanel && !insideIcon) {
        // если клик был произведен вне панели с навигационными ссылками и вне иконки меню, это означает, что пользователь кликнул за пределами мобильного меню, и мы должны закрыть его
        closeMenu(); // закрываем мобильное меню, удаляя класс openClass у контейнера с навигационными ссылками
      }
    }),
  );

  // nav scrolled state
  const applyScrolled = () => {
    // функция для применения класса scrolledClass к навбару в зависимости от текущей прокрутки страницы
    toggleClass(nav, scrolledClass, getScrollY() > (scrolledThreshold ?? 55)); // добавляем класс scrolledClass к навбару, если текущая прокрутка страницы больше порога scrolledThreshold (или 55 пикселей по умолчанию), иначе удаляем этот класс
  };
  applyScrolled(); // вызываем функцию applyScrolled при инициализации, чтобы установить правильное состояние навбара в зависимости от текущей прокрутки страницы
  d.add(on(window, "scroll", applyScrolled, { passive: true })); // добавляем обработчик события прокрутки на окно для вызова функции applyScrolled при прокрутке страницы, используем passive: true для оптимизации производительности

  // smooth scrolling for anchor links
  if (document.body.classList.contains("website")) {
    // если тело документа содержит класс "website", добавляем плавную прокрутку для якорных ссылок
    navItems.forEach((item) => {
      d.add(
        // добавляем обработчик события клика на каждый пункт меню для плавной прокрутки к соответствующему разделу страницы
        on(item, "click", (e) => {
          // при клике на пункт меню
          const href = item.getAttribute("href") || ""; // получаем значение атрибута href у пункта меню, или пустую строку, если атрибут отсутствует
          if (!href.startsWith("#")) return; // если href не начинается с "#", это не якорная ссылка, и мы не обрабатываем ее

          const targetEl = document.querySelector(href); // находим элемент на странице, к которому нужно прокрутиться, используя значение href в качестве селектора
          if (!targetEl) return; // если элемент не найден, прекращаем обработку

          e.preventDefault(); // предотвращаем стандартное поведение браузера при клике на якорную ссылку, чтобы мы могли реализовать плавную прокрутку вручную

          const y = // вычисляем координату Y для прокрутки, учитывая позицию целевого элемента и возможное смещение
            targetEl.getBoundingClientRect().top + // получаем верхнюю координату целевого элемента относительно видимой области окна
            window.pageYOffset - // добавляем текущую вертикальную прокрутку страницы, чтобы получить абсолютную координату элемента на странице
            (scrollOffset ?? 0); // вычитаем значение scrollOffset (или 0 по умолчанию), чтобы учесть возможное смещение при прокрутке, например, если навбар фиксирован и перекрывает часть контента
          scrollToY(y, { behavior: "smooth" }); // выполняем плавную прокрутку к вычисленной координате Y

          // update URL without jumping
          history.pushState(null, "", href); // обновляем URL в адресной строке без перезагрузки страницы и без прыжка к якорю, используя метод history.pushState

          if (isMobile(mobileBreakpoint)) {
            // если устройство является мобильным (ширина экрана меньше mobileBreakpoint), закрываем мобильное меню после клика на якорную ссылку
            closeMenu(); // закрываем мобильное меню, чтобы пользователь мог видеть контент, к которому он прокрутился, без необходимости вручную закрывать меню
          }
        }),
      );
    });
  }

  return () => d.dispose(); // возвращаем функцию для очистки обработчиков событий и классов при уничтожении компонента, вызывая метод dispose объекта d, который удаляет все добавленные обработчики и классы
}
